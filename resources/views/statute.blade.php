                            فراخوانی مجمع عمومی و انتخاب هیأت مدیره جدید اقدام و بلافاصله نتایج را همراه با صورت جلسه
                            مجمع عمومی برای بررسی و تایید کمیسیون انجمن‌های علمی ارسال نماید.
                        </p>
