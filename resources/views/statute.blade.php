                            موافق معتبر است.
                        </p>
                        <p>
                            12-7- کلیه مصوبات هیأت مدیره ثبت و پس از امضا در دفتر صورتجلسات هیأت مدیره نگهداری می‌شود.
                        </p>
                        <p>
