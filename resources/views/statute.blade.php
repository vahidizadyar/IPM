                        </p>
                        <p>
                            6-2- وابسته

                            کلیه افراد و سازمان‌های علاقمند به رشته و حرفه مدیریت پروژه می‌توانند به عضویت وابسته انجمن
                            درآیند.
                        </p>
                        <p>
                            6-3- دانشجویی
                        </p>
                        <p>
                            کلیه دانشجویان علاقمند به دانش و حرفه مدیریت پروژه می‌توانند به عضویت داشجویی انجمن درآیند.

                            تبصره 3: اعضای وابسته و دانشجویی می‌توانند بدون حق رأی براساس آیین‌نامه مصوب هیأت مدیره به
                            عنوان ناظر در جلسات مجمع عمومی شرکت کنند.
                        </p>
                        <p>
                            6-4- اعضا حقوقی
                        </p>
                        <p>
                            سازمان‌هایی که در زمینه علمی، پژوهشی مربوطه و فنی مهندسی و مشاوره فعالیت دارند می‌توانند به
                            عضویت انجمن درآیند.
                        </p>
                        <p>
                            تبصره 3: اعضا حقوقی به مثابه عضو وابسته انجمن محسوب می‌شوند.
