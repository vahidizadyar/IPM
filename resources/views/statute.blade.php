                            نوبت دوم با حضور هر تعداد از اعضای انجمن رسمیت می‌یابد و تصمیمات با اکثریت آراء معتبر است.
                        </p>
                        <p>
                            10-2- مجمع عمومی فوق‌العاده در موارد ضروری، با دعوت هیأت مدیره یا بازرس (بازرسان) و یا با
                            تقاضای کتبی یک سوم اعضای اصلی تشکیل می شود.
                        </p>
                        <p>
                            10-3- در موضوعات مصوب مجمع عمومی، اخذ رأی بصورت مکاتبه‌ای یا الکترونیکی و براساس آیین
                            نامه‌هایی که توسط هیأت مدیره تدوین می‌شود مجاز می‌باشد.
                        </p>
                        <p>
                            تبصره 9:  دعوت برای تشکیل مجامع عمومی به صورت کتبی یا آگهی الکترونیکی در سایت انجمن و آگهی
                            در روزنامه رسمی مصوب مجمع صورت می گیرد و باید حداقل پانزده روز قبل از تشکیل مجمع به اطلاع
                            اعضای اصلی برسد.
                        </p>
                        <p>
                            تبصره 10:  یک سوم اعضای اصلی می‌توانند مستقیماً اقدام به دعوت برای تشکیل مجمع عمومی
                            فوق‌العاده نمایند مشروط بر اینکه هیأت مدیره و نیز بازرسان انجمن به تقاضای ایشان پاسخ منفی
                            داده باشد و در چنین حالتی ایشان باید در آگهی دعوت به عدم اجابت درخواست خود توسط هیأت مدیره
                            یا بازرسان تصریح نمایند.
                        </p>
                        <p>
                            تبصره 11: در صورت تحقق تبصره فوق، دستور جلسه عمومی منحصراً موضوعی خواهد بود که درتقاضامه
                            ذکرشده است.
                        </p>
