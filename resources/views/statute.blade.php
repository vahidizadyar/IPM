                            بازرس کاندیدا کرده‌اند.
                        </p>
                        <p>
                            تبصره 16:  جلسات مجامع عمومی عادی یا فوق‌العاده با دعوت و حضور نماینده کمیسیون انجمنهای علمی
                            ایران رسمیت می‌یابد.
