                            مدیریت پروژه و یا پیشبرد اهداف انجمن کرده باشند پس از تصویب هیأت مدیره، با داشتن حق رأی به
                            عضویت افتخاری انجمن پذیرفته می شوند. این عضو نمی‌تواند کاندید عضویت هیأت مدیره انتخاب شود.
