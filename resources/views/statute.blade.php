                            صلاحیت خاص مجامع عمومی است، هیأت مدیره کلیه اختیارات لازم برای اداره امور، مشروط به رعایت
                            حدود موضوع فعالیت را دارا می‌باشد.
                        </p>
                        <p>
                            13-5- تهیه گزارش سالانه و تنظیم ترازنامه مالی و صورتحساب درآمدها و هزینه‌های انجمن برای
                            تصویب در مجمع عمومی و ارائه به مرجع نظارت در موقع مقرر
                        </p>
                        <p>
                            13-6- اقامه دعوی و پاسخگویی به دعاوی اشخاص حقیقی یا حقوقی در تمام مراجع و مراحل دادرسی با حق
                            تعیین وکیل و حق توکیل به غیر
                        </p>
