                            <br>
                            جایزه پژوهش برتر مدیریت پروژه ایران
                            <br>
                            انجمن بین المللی مدیریت پروژه (IPMA) با بیش از 55 عضو از انجمن های مدیریت پروژه دنیا و با سابقه طولانی بیش از چهل سال، پیشرو جامعه جهانی در گسترش مدیریت پروژه در سطح دنیاست و انجمن مدیریت پروژه ایران به عنوان یکی از اعضای دائم آن فعالیت می کند.
                            <br>
                            جایزه پژوهش برتر مدیریت پروژه یکی از جوایزی است که هر ساله در بسیاری از کشورهای عضو انجمن بین المللی مدیریت پروژه (IPMA) با هدف کمک به توسعه و ترویج پژوهش در جهت بهبود اصول، مبانی و تکنیکهای مدیریت پروژه اعطا می شود. انجمن مدیریت پروژه ایران با حمایت IPMA جهانی، این جایزه را طرح ریزی و اجرا می نماید، با این امید که پژوهش هایی که در کشور ما –چه در حیطه دانشگاه و چه در حوزه های صنعتی- در راستای بالندگی و تعالی مدیریت پروژه گام بر می دارند، برای رقابت در این جایزه شرکت نموده و بدین ترتیب از برترین پژوهش ها تقدیری شایسته به عمل آید.
